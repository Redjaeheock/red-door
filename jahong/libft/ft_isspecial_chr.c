#include "../minishell.h"

int	ft_isspecial_chr(int c)
{
	if ('!' <= c && c <= '/')
		return (1);
	if (c == ':' || c == ';')
		return (1);
	if (c == '=' || c == '@')
		return (1);
	if ('[' <= c && c <= '^')
		return (1);
	if (c == '`' || ('{' <= c && c <= '~'))
		return (1);
	return (0);
}